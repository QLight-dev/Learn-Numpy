import numpy as np

# Sorting Arrays
array = np.array([6, 2, 4, 8, 7, 9])
print(f"Sorting Arrays: {np.sort(array)}")

# Sorting a String Array
array = np.array(["Chocolate", "Ice Cream", "Yo-yo", "Groceries"])
print(f"Sorting a String Array: {np.sort(array)}")

# Sorting a 2-D Array
array = np.array([[6, 2, 9], [5, 8, 3]])
print(f"Sorting a 2-D Array: {np.sort(array)}")